{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('dashboard.users.index', [
            'title' => 'All Users',
            'users' => User::paginate(12)->withQueryString()
        ]);
    }

    /**
